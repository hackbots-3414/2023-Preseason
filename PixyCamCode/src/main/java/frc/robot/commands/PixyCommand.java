package frc.robot.commands;

import edu.wpi.first.wpilibj2.command.CommandBase;
import edu.wpi.first.wpilibj2.command.SubsystemBase;
import frc.robot.subsystems.Pixycam;
import io.github.pseudoresonance.pixy2api.Pixy2;
import io.github.pseudoresonance.pixy2api.Pixy2CCC;
import io.github.pseudoresonance.pixy2api.Pixy2Video;
import io.github.pseudoresonance.pixy2api.Pixy2CCC.Block;
import io.github.pseudoresonance.pixy2api.links.SPILink;

public class PixyCommand extends CommandBase {
  Pixycam Pixycam;
  /** Creates a new PixyCommand. */
  public PixyCommand(Pixycam pixBase) {
    // Use addRequirements() here to declare subsystem dependencies.
    addRequirements(pixBase);
    this.Pixycam = pixBase;
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {}

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    Block biggestBlock  = this.Pixycam.getBiggestBlock();

    System.out.println("largestBlock: " + biggestBlock.getSignature());
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {}

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
